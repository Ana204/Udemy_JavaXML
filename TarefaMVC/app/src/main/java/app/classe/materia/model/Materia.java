package app.classe.materia.model;

public class Materia {

    private int CodigoDaMateria;
    private String NomeDaMateria;

    public String getNomeDaMateria() {
        return NomeDaMateria;
    }

    public void setNomeDaMateria(String nomeDaMateria) {
        NomeDaMateria = nomeDaMateria;
    }

    public int getCodigoDaMateria() {
        return CodigoDaMateria;
    }

    public void setCodigoDaMateria(int codigoDaMateria) {
        CodigoDaMateria = codigoDaMateria;
    }
}
